    private void jButtonAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarProductoActionPerformed

    //Metodo Agregar Productos 
        Productos product1 = new Productos();
        if(product1.getCodigo() == 1)
        {
          
          product1.setPrecio_a_pagar(product1.getCantidadp()* product1.getPrecio_unitario());
          product1.setPrecio_total(product1.getPrecio_total()+product1.getPrecio_a_pagar());
          JOptionPane.showMessageDialog(null, "El valor total de su compra es: " + product1.getPrecio_total());
          
        }
        else if (product1.getCodigo()==2)
        {
          product1.setPrecio_a_pagar(product1.getCantidadp()* product1.getPrecio_unitario());
          product1.setPrecio_total(product1.getPrecio_total()+product1.getPrecio_a_pagar());
          JOptionPane.showMessageDialog(null, "El valor total de su compra es: " + product1.getPrecio_total());           
        }
        else if (product1.getCodigo()==3)
        {
          product1.setPrecio_a_pagar(product1.getCantidadp()* product1.getPrecio_unitario());
          product1.setPrecio_total(product1.getPrecio_total()+product1.getPrecio_a_pagar());
          JOptionPane.showMessageDialog(null, "El valor total de su compra es: " + product1.getPrecio_total());   
        }
        else if (product1.getCodigo()==4)
        {
          product1.setPrecio_a_pagar(product1.getCantidadp()* product1.getPrecio_unitario());
          product1.setPrecio_total(product1.getPrecio_total()+product1.getPrecio_a_pagar());
          JOptionPane.showMessageDialog(null, "El valor total de su compra es: " + product1.getPrecio_total());   
        }
        else
        {
         JOptionPane.showMessageDialog(null, "El codigo es incorrecto");
        }
    }//GEN-LAST:event_jButtonAgregarProductoActionPerformed

