    private void jButtonEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarProductoActionPerformed
        //Metodo Eliminar Productos 
        Productos product1 = new Productos();
        if(product1.getCodigo() == 1 || product1.getCodigo()== 2 || product1.getCodigo()== 3 || product1.getCodigo()== 4)
        {
          
          product1.setPrecio_total(product1.getPrecio_total()-product1.getPrecio_unitario());
          
          JOptionPane.showMessageDialog(null, "El valor total de su compra es: " + product1.getPrecio_total());
          
        }
    }//GEN-LAST:event_jButtonEliminarProductoActionPerformed

